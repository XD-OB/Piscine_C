int		ft_antidote(int i, int j, int k)
{
	int		tmp;

	if (i > j)
	{
		tmp = i;
		i = j;
		j = tmp;
	}
	if (j > k)
	{
		tmp = j;
		j = k;
		k = tmp;
	}
	if (i > j)
	{
		tmp = i;
		i = j;
		j = tmp;
	}
	return (j);
}
